##Leia um valor real e a cotação do dólar. Em seguida, imprima o valor correspondente em dólares.

valorReais = float(input("Digite o valor em reais: "))
cotacao_dolar = float(input("Digite a cotação do dolar: "))
valorDolar = valorReais / cotacao_dolar

print(f"O valor correspondente em dolares eh: ${valorDolar:.2f}")




